# The Ruby Array class has several methods for removing items from the array. 
# Two of them have very similar names. Let's see how they differ:

numbers = [1, 2, 3, 4, 5]

numbers.delete_at(1) # this will delete the value at index number one which is 2
numbers.delete(1) # this will delete the value 1, which is in index number zero 



